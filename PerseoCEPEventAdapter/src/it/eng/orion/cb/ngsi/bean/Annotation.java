package it.eng.orion.cb.ngsi.bean;

public class Annotation {

	private String annotationInstance;

	private String annotationId;

	private String annotationDes;

	public String getAnnotationInstance() {
		return annotationInstance;
	}

	public void setAnnotationInstance(String annotationInstance) {
		this.annotationInstance = annotationInstance;
	}

	public String getAnnotationId() {
		return annotationId;
	}

	public void setAnnotationId(String annotationId) {
		this.annotationId = annotationId;
	}

	public String getAnnotationDes() {
		return annotationDes;
	}

	public void setAnnotationDes(String annotationDes) {
		this.annotationDes = annotationDes;
	}

}
