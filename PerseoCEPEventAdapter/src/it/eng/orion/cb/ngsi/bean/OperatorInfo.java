package it.eng.orion.cb.ngsi.bean;

public class OperatorInfo {
	
	private String operatorId;
	
	private String operatorName;
	
	private String operatorInstanceName;
	
	private String workOrder;
	
	private String processSegment;
	
	public String getOperatorId() {
		return operatorId;
	}

	public void setOperatorId(String operatorId) {
		this.operatorId = operatorId;
	}

	public String getOperatorName() {
		return operatorName;
	}

	public void setOperatorName(String operatorName) {
		this.operatorName = operatorName;
	}

	public String getOperatorInstanceName() {
		return operatorInstanceName;
	}

	public void setOperatorInstanceName(String operatorInstanceName) {
		this.operatorInstanceName = operatorInstanceName;
	}

	public String getWorkOrder() {
		return workOrder;
	}

	public void setWorkOrder(String workOrder) {
		this.workOrder = workOrder;
	}

	public String getProcessSegment() {
		return processSegment;
	}

	public void setProcessSegment(String processSegment) {
		this.processSegment = processSegment;
	}
	
	

}
